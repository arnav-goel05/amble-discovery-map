#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const pointer = JSON.parse(
  fs.readFileSync(path.join(root, "data/approved-snapshot.json"), "utf8"),
);
const snapshotRoot = path.join(root, "data/snapshots", pointer.snapshotId);
const manifest = JSON.parse(
  fs.readFileSync(path.join(snapshotRoot, "manifest.json"), "utf8"),
);

if (manifest.snapshotId !== pointer.snapshotId)
  throw new Error("Approved snapshot pointer and manifest do not match");

const readAsset = (reference) =>
  JSON.parse(fs.readFileSync(path.join(snapshotRoot, reference), "utf8"));
const payload = {
  pointer,
  manifest,
  assets: {
    [manifest.landmarksRef]: readAsset(manifest.landmarksRef),
    [manifest.poisRef]: readAsset(manifest.poisRef),
    [manifest.tilesetRef]: readAsset(manifest.tilesetRef),
    ...(manifest.eventsRef
      ? { [manifest.eventsRef]: readAsset(manifest.eventsRef) }
      : {}),
  },
};
const output = path.join(root, "cloudflare/generated-approved-snapshot.mjs");
fs.writeFileSync(
  output,
  `// Generated by scripts/generate-cloudflare-snapshot.mjs.\nexport const APPROVED_SNAPSHOT = ${JSON.stringify(payload)};\n`,
);
console.log(`Prepared Cloudflare snapshot ${manifest.snapshotId}.`);
